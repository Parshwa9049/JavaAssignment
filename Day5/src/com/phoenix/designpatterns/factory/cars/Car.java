package com.phoenix.designpatterns.factory.cars;
//Class Car
public class Car {
	
    //Start method
	public void start()
	{
	System.out.println("Car Starts");	
	}
	//Stop method
	public void stop()
	{
		System.out.println("Car Stops");			
	}
	//Run method
	public void run()
	{			
		System.out.println("Car Runs");
	}
}
